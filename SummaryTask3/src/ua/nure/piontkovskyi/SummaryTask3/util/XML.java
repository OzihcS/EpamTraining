package ua.nure.piontkovskyi.SummaryTask3.util;

/**
 * Enum which contains names of attributes in XML
 */
public enum XML {

    DEPOSITS("Deposits"),BANK("Bank"), NAME("Name"), COUNTRY("Country"), TYPE("Type"), DEPOSITOR("Depositor"), ACCOUNT_ID("AccountId"),
    AMOUNT_ON_DEPOSIT("AmountOnDeposit"), PROFITABILITY("Profitability"), TIME_CONSTRAINTS("TimeConstraints");

    private String value;

    XML(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public boolean isEqual(String name) {
        return value.equals(name);
    }

}
